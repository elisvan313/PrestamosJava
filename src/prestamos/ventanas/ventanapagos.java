    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codigoprestamo = tfbprest.getText();
        tbprestamosdatos tbpres = new tbprestamosdatos();
        if (codigoprestamo.isEmpty() == true) {
            JOptionPane.showMessageDialog(this, "Digite el codigo del prestamo a buscar");
        } else {
            Integer codigopres = null;
            codigopres = new Integer(codigoprestamo);
            prestamo = tbpres.Buscarprestamo(codigopres);
            if (prestamo == null) {
                JOptionPane.showMessageDialog(this, "El prestamo con codigo " + codigopres + " no existe");
            } else {
                lbsaldoactual.setText(String.valueOf(prestamo.getNcuotas() * prestamo.getValorcuota()));
                lbcuotaspendientes.setText(String.valueOf(prestamo.getNcuotas()));

                //fechapago.setDate(prestamo.getFechaprestamo());
                lbncliente.setText(prestamo.getNombrec());
                lbvcuota.setText(String.valueOf(prestamo.getValorcuota()));
                tfcuotaspagar.setEditable(true);
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void tfcuotaspagarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfcuotaspagarKeyReleased
        try {
            if (!tfcuotaspagar.getText().isEmpty() && !lbvcuota.getText().isEmpty()) {
                if (Integer.parseInt(lbcuotaspendientes.getText()) < Integer.parseInt(tfcuotaspagar.getText())) {
                    JOptionPane.showMessageDialog(this, "Digite un numero valido de cuotas a pagar");
                } else {
                    Integer totalpagar = Integer.parseInt(tfcuotaspagar.getText()) * Integer.parseInt(lbvcuota.getText());
                    lbvalortotalpagar.setText(totalpagar + "");
                    btaceptarp.setEnabled(true);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Digite solo numeros");
        }

    }//GEN-LAST:event_tfcuotaspagarKeyReleased

