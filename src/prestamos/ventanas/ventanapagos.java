    private void btaceptarpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaceptarpActionPerformed
        tbprestamosdatos prest = new tbprestamosdatos();
        tbpagosdatos pagosdatos = new tbpagosdatos();
        ventanaprestamos vprest = new ventanaprestamos();
        JCalendar fechavencerc = fechapago.getJCalendar();
        String cbprestamo = prestamo.getTipoprestamo();
        switch (cbprestamo) {
            case "Diario":

                Calendar fpres = fechapago.getCalendar();
                fpres.add(Calendar.DATE, 1);

                fechavencerc.setCalendar(fpres);

                break;
            case "Semanal":
                fpres = fechapago.getCalendar();
                fpres.add(Calendar.DATE, 7);
                fechavencerc.setCalendar(fpres);

                break;
            case "Quincenal":
                fpres = fechapago.getCalendar();
                fpres.add(Calendar.DATE, 15);
                fechavencerc.setCalendar(fpres);

                break;
            case "Mensual":
                fpres = fechapago.getCalendar();
                fpres.add(Calendar.DATE, 30);
                fechavencerc.setCalendar(fpres);

                break;
            default:

        }
       
        Integer cuotaspendientes = Integer.parseInt(lbcuotaspendientes.getText()) - Integer.parseInt(tfcuotaspagar.getText());
        prest.Editarprestamo(prestamo.getCodigo(),prestamo.getNombrec(),prestamo.getValorprestamo(), cuotaspendientes, prestamo.getInteres(), prestamo.getTipoprestamo(), prestamo.getValorcuota(), prestamo.getFechaprestamo(), fechavencerc.getDate());
        if (cuotaspendientes == 0) {
            
            int resp = JOptionPane.showConfirmDialog(this, "¿Si realiza el pago quedarian 0 cuotas pendientes, ¿desea eliminar el prestamo??", "Notificacion!", JOptionPane.YES_NO_OPTION);
            if (resp == 0) {
                try {
                    prest.Eliminarprestamo(Integer.parseInt(tfbprest.getText()));
                    JOptionPane.showMessageDialog(this, prest.mensaje);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error al eliminar");
                }

            }
        }
       Integer sactual = Integer.parseInt(lbsaldoactual.getText()) - Integer.parseInt(lbvcuota.getText()) * Integer.parseInt(tfcuotaspagar.getText());
        pagosdatos.Realizarpago(Integer.parseInt(tfbprest.getText()),lbncliente.getText(), sactual, Integer.parseInt(tfcuotaspagar.getText()), cuotaspendientes, fechapago.getDate());
        JOptionPane.showMessageDialog(this, pagosdatos.mensaje);
        limpiar();

    }//GEN-LAST:event_btaceptarpActionPerformed
